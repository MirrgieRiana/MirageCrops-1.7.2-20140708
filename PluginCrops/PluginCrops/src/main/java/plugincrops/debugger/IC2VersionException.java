package plugincrops.debugger;

public class IC2VersionException extends Exception
{

	public IC2VersionException()
	{
		super();
	}

	public IC2VersionException(String message, Throwable cause)
	{
		super(message, cause);
	}

	public IC2VersionException(String message)
	{
		super(message);
	}

	public IC2VersionException(Throwable cause)
	{
		super(cause);
	}

}
